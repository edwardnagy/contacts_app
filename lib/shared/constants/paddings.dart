/// Padding values following the Material Design guidelines.
/// `8.0` is the base padding value.
///
/// See: https://material.io/design/layout/spacing-methods.html#spacing
class Paddings {
  const Paddings._();

  static const base = 8.0;

  static const x0_25 = base / 4;
  static const x0_5 = base / 2;
  static const x1 = base;
  static const x1_5 = base * 1.5;
  static const x2 = base * 2;
  static const x3 = base * 3;
  static const x4 = base * 4;

  static const firstKeyline = x2;
}
